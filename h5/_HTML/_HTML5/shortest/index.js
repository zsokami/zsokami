function shortestPath(a, b) {
	//记录步数
	var step = [];
	for (var i = 0; i < h; i++) step[i] = [];
	step[a.y][a.x] = 1;
	//待遍历点
	var points = [{ x: a.x, y: a.y }];
	while (points.length > 0) {
		var i = 0;
		var min = 0x7FFFFFFF;
		for (var k = 0; k < points.length; k++) {
			var x = points[k].x;
			var y = points[k].y;
			var j = step[y][x] + distance(x, y, b.x, b.y);
			if (j < min) {
				min = j;
				i = k;
			}
		}
		var x = points[i].x;
		var y = points[i].y;
		points.splice(i, 1);
		var s = step[y][x] + 1;
		for (var d = 0; d < 16; d += 2) {
			var nx = x + dir[d];
			var ny = y + dir[d + 1];
			if (isOK(nx, ny)) {

				if (nx == b.x && ny == b.y) {
					x = nx;
					y = ny;
					var path = [{ x: x, y: y }];
					while (s-- > 2) {
						var ds = [];
						for (d = 0; d < 16; d += 2) {
							nx = x + dir[d];
							ny = y + dir[d + 1];
							if (isOK(nx, ny) && step[ny][nx] == s)
								ds.push(d);
						}
						if (ds.length > 1) {
							for (d = 0; d < ds.length; d++) {
								nx = x + dir[ds[d]] * 2;
								ny = y + dir[ds[d] + 1] * 2;
								if (isOK(nx, ny) && step[ny][nx] == s - 1) {
									ds[0] = ds[d];
									break;
								}
							}
						}
						x += dir[ds[0]];
						y += dir[ds[0] + 1];
						path.unshift({ x: x, y: y });
					}
					return path;
				}

				var ns = step[ny][nx];
				if (!ns) {
					points.push({ x: nx, y: ny });
				} else if (ns <= s) continue;
				step[ny][nx] = s;
			}
		}
	}
	return null;
}

function isOK(x, y) {
	return x >= 0 && y >= 0 && x < w && y < h && map[y][x] != 1;
}

function distance(x1, y1, x2, y2) {
	return Math.abs(x1 - x2) + Math.abs(y1 - y2);
}
